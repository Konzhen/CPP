Cat::Cat()
{
	setType("Cat");
	std::cout << "Oh no... Honey ? Who let this devil come in our house ?!" << std::endl;
}

Cat::Cat(Cat const &cat)
{
	_type = cat.getType();
}

Cat::~Cat()
{
	std::cout << "Finally, this little brat left us." << std::endl;
}


//--------------------------------------------------------------------------//
//	 	                      - OPERATORS -	    						    //
//--------------------------------------------------------------------------//

Cat    &Cat::operator=(Cat const &cat)
{
	_type = cat.getType();
	return (*this);
}


